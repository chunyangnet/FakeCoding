def build_expansion_review(title, question, module, step_index, review_index):
    heading, action, risk, next_step = EXPANSION_REVIEWS[review_index % len(EXPANSION_REVIEWS)]
    return (
        f"#### {heading} · 检查项 {review_index + 1}\n\n"
        f"围绕“{title}”的第 {step_index + 1} 个阶段，我把当前 `{module}` 工作进一步收窄到一个可验证问题："
        f"如何在不偏离“{question}”的前提下获得新的区分信息。{action}\n\n"
        f"**当前风险**：{risk}\n\n"
        "- [x] 当前检查项与主任务存在直接关系\n"
        "- [ ] 新观察能够区分至少两个候选解释\n"
        "- [ ] 失败时可以回到上一稳定状态\n\n"
        f"> **后续动作**：{next_step}"
    )


def compile_preset(preset, minimum_chars=12000):
    """Return a deterministic long-form copy of one preset."""
    compiled = deepcopy(preset)
    title = str(compiled.get("title", "未命名任务"))
    question = str(compiled.get("question", title))
    steps = compiled.get("steps", [])
    for index, step in enumerate(steps):
        module = step.get("module", "research")
        module_title = MODULE_TITLES.get(module, MODULE_TITLES["research"])
        module_focus = MODULE_FOCUS.get(module, MODULE_FOCUS["research"])
        details = MODULE_DETAILS.get(module, MODULE_DETAILS["research"])
        module_detail = details[index % len(details)]
        validation = VALIDATION_DETAILS[index % len(VALIDATION_DETAILS)]
        transition = TRANSITIONS[index % len(TRANSITIONS)]
        checklist = MODULE_CHECKLISTS.get(module, MODULE_CHECKLISTS["research"])
        original_text = str(step.get("text", "")).strip()
        context = CONTEXT_OPENERS[index % len(CONTEXT_OPENERS)].format(title=title, question=question)
        stage_quote = (
            f"> **任务主题**：{title}\n>\n> **当前问题**：{question}\n\n"
            if index == 0
            else f"> **阶段目标**：{module_title}\n>\n> **承接状态**：沿用前一阶段已确认的边界，只处理当前新增证据。\n\n"
        )
        step["text"] = (
            f"### 阶段 {index + 1}/{len(steps)} · {module_title}\n\n"
            f"{stage_quote}"
            "#### 当前动作\n\n"
            f"- {original_text}\n"
            f"- **检查重点**：{module_focus}\n"
            "- **完成条件**：获得可以被下一步工具或测试复核的中间结论\n\n"
            "```text\n"
            "status: in_progress\n"
            f"module: {module}\n"
            "upstream_tokens: 0\n"
            "```\n\n"
            f"#### 工作记录\n\n{context}{module_detail}\n\n"
            "#### 验证清单\n\n"
            f"- [x] {checklist[0]}\n"
            f"- [ ] {checklist[1]}\n"
            f"- [ ] {checklist[2]}\n\n"
            f"{validation}\n\n> **下一步**：{transition}"
        )

    def script_length():
        return sum(len(str(step.get("text", ""))) for step in steps)

    padding_index = 0
    while steps and script_length() < minimum_chars:
        step_index = padding_index % len(steps)
        target = steps[step_index]
        target["text"] += "\n\n" + build_expansion_review(
            title,
            question,
            str(target.get("module", "research")),
            step_index,
            padding_index,
        )
        padding_index += 1
    compiled["compiled_chars"] = script_length()
    compiled["total_chars"] = compiled["compiled_chars"] + len(str(compiled.get("closing", "")))
    return compiled
